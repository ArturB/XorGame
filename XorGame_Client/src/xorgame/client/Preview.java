    private void titlePreviewButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_titlePreviewButtonActionPerformed
        String newName = JOptionPane.showInputDialog(this, "Enter new name of planet" + main.getSelectedPlanet().getName());
        if(!newName.isEmpty()) {
            try {
                main.getMainControler().changePlanetName(main.getSelectedPlanet().getPlanetPK(), newName);
                titlePreviewButton.setText(newName);
                main.getMaterials().updateMaterials();
                main.getMaterials().updateComboBox();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Error while changing planet name!", "Error", JOptionPane.ERROR_MESSAGE);
                Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_titlePreviewButtonActionPerformed

    private void abandonButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_abandonButtonActionPerformed
        if(JOptionPane.showConfirmDialog(main, "Do you really want to abandon this planet?", "Really?", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            try { 
                main.getMainControler().abandonPlanet(main.getSelectedPlanet().getPlanetPK());
                main.getPreview().updatePreview();
                JOptionPane.showMessageDialog(main, "Planet successfully abandoned!"); 
                main.getMaterials().updateComboBox();
            } catch (SQLException ex) {
                Logger.getLogger(Preview.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_abandonButtonActionPerformed


