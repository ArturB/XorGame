    /**
     * Updates user nick, user statistics and currently built orders. 
     */
    public void updatePreview() {
        titlePreviewButton.setText(main.getSelectedPlanet().getName());
        
        planetImagePreviewLabelComponentResized(null);
        
        usernamePreviewLabel.setText(main.getUser().getNick()); 
        totalPointsPreviewLabel.setText(Integer.toString(main.getMainControler().getTotalPoints(main.getUser().getNick())));
        economyPointsPreviewLabel.setText(Integer.toString(main.getMainControler().getEconomyPoints(main.getUser().getNick())));
        researchPointsPreviewLabel.setText(Integer.toString(main.getMainControler().getResearchPoints(main.getUser().getNick())));
        militaryPointsPreviewLabel.setText(Integer.toString(main.getMainControler().getMilitaryPoints(main.getUser().getNick())));
        
        buildingOrdersList.removeAll();
        researchOrdersList.removeAll();
        shipyardOrdersList.removeAll();
        flightsList.removeAll();
        
        DefaultListModel buildings = new DefaultListModel();
        for(BuildingOrder ord : main.getSelectedPlanet().getBuildingOrderSet()) 
            buildings.addElement(ord.toString());
        buildingOrdersList.setModel(buildings);
        
        DefaultListModel research = new DefaultListModel();
        for(ResearchOrder ord : main.getSelectedPlanet().getResearchOrderSet()) 
            research.addElement(ord.toString());
        researchOrdersList.setModel(research);
        
        DefaultListModel shipyard = new DefaultListModel();
        for(ShipyardOrder ord : main.getSelectedPlanet().getShipyardOrderSet()) 
            shipyard.addElement(ord.toString());
        shipyardOrdersList.setModel(shipyard);
        
        DefaultListModel flights = new DefaultListModel();
        for(Flight ord : main.getSelectedPlanet().getFlightSet()) 
            flights.addElement(ord.toString());
        
        for(Flight ord : main.getSelectedPlanet().getFlightSet1())
            flights.addElement(ord.toString());
        flightsList.setModel(flights);
    }

