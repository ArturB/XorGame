    private void planetImagePreviewLabelComponentResized(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_planetImagePreviewLabelComponentResized
        try {
            byte[] plImg = null;

            if(main.getSelectedPlanet() != null) {
                for(PlanetGui img: main.getGui().getPlanetGui()) {
                    if(img.getId() == main.getSelectedPlanet().getGui()) {
                        plImg = img.getImage();
                        break;
                    }
                }

                Image scaled;
                BufferedImage planetImage = ImageIO.read(new ByteArrayInputStream(plImg));
                if(planetImagePreviewLabel.getWidth() < planetImagePreviewLabel.getHeight()) {
                    scaled = planetImage.getScaledInstance(
                        planetImagePreviewLabel.getWidth(),
                        planetImage.getHeight() * planetImagePreviewLabel.getWidth() / planetImage.getWidth(),
                        Image.SCALE_DEFAULT );
                }
                else {
                    scaled = planetImage.getScaledInstance(
                        planetImage.getWidth() * planetImagePreviewLabel.getHeight() / planetImage.getHeight(),
                        planetImagePreviewLabel.getHeight(),
                        Image.SCALE_FAST );
                }

                planetImagePreviewLabel.setIcon(new ImageIcon(scaled));
            }
        } catch (IOException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_planetImagePreviewLabelComponentResized

